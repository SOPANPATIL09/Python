#Assignment operators =,+=,-=,/=,*=,%=,**=

num1=10
num1 += 10 #10+10=20
print("num1:",num1)


num2=10
num2 -= 2 #10-2=8
print("num2:",num2)


num3=10
num3 /= 2 #10/2=5
print("num3:",num3)

num4=10
num4 *= 2 #10*2=20
print("num4:",num4)


num5=10
num5 %= 2 #10%2=0
print("num5:",num5)

num6=10
num6 **= 2 #10**2=100
print("num6:",num6)
